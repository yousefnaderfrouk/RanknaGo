  static String getLanguageNameFromLocale(Locale locale) {
    for (var entry in languageMap.entries) {
      if (entry.value.languageCode == locale.languageCode) {
        if (locale.countryCode != null) {
          if (entry.value.countryCode == locale.countryCode) {
            return entry.key;
          }
        } else {
          return entry.key;
        }
      }
    }
    return 'English (US)';
  }

