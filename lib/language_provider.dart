import 'package:flutter/material.dart';

// Extension to easily access translations from any widget
extension TranslationExtension on BuildContext {
  String translate(String key) {
    final languageProvider = LanguageProvider.of(this);
    final locale = languageProvider?.locale ?? const Locale('en', 'US');
    return AppLanguages.translate(key, locale);
  }
}

class LanguageProvider extends InheritedWidget {
  final Locale locale;
  final Function(Locale) changeLanguage;

  const LanguageProvider({
    super.key,
    required this.locale,
    required this.changeLanguage,
    required super.child,
  });

  static LanguageProvider? of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<LanguageProvider>();
  }

  @override
  bool updateShouldNotify(LanguageProvider oldWidget) {
    return locale != oldWidget.locale;
  }
}

